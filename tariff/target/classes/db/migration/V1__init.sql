CREATE TABLE IF NOT EXISTS tariff (
  id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  hs_code          VARCHAR(10)         NOT NULL,
  importer_id      VARCHAR(2)          NOT NULL,
  exporter_id      VARCHAR(2)          NOT NULL,
  tariff_type      VARCHAR(20)         NOT NULL,   
  tariff_rate      DOUBLE PRECISION,
  specific_amt     DOUBLE PRECISION,
  specific_unit    VARCHAR(20),
  min_tariff_amt   DOUBLE PRECISION,
  max_tariff_amt   DOUBLE PRECISION,
  start_date       DATE                NOT NULL,
  end_date         DATE                NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_tariff_hs_imp_exp ON tariff (hs_code, importer_id, exporter_id);
CREATE INDEX IF NOT EXISTS idx_tariff_dates ON tariff (start_date, end_date);
